let sentAudios = {};

function handleMusicPlayback(data, chatId) {
   const index = data.split('_')[1];
   fs.readdir(musicDirectory, (err, files) => {
      if (err) {
         console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ —á—Ç–µ–Ω–∏–∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –º—É–∑—ã–∫–∏:', err);
         return bot.sendMessage(chatId, '–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ –º—É–∑—ã–∫–∏ ü•≤.');
      }
      if (index >= files.length) {
         return bot.sendMessage(chatId, '–í—ã–±—Ä–∞–Ω–Ω—ã–π —Ç—Ä–µ–∫ –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –∫–∞—Ç–∞–ª–æ–≥–µ ü§î.');
      }
      const selectedFile = files[index];
      const musicPath = path.join(musicDirectory, selectedFile);

      // –£–¥–∞–ª—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π –∞—É–¥–∏–æ—Ñ–∞–π–ª, –µ—Å–ª–∏ –æ–Ω —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
      if (sentAudios[selectedFile]) {
         bot.deleteMessage(chatId, sentAudios[selectedFile]).catch(error => console.log('–û—à–∏–±–∫–∞ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ –∞—É–¥–∏–æ:', error.message));
      }

      // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∞—É–¥–∏–æ—Ñ–∞–π–ª
      bot.sendAudio(chatId, musicPath, {
         contentType: 'audio/mpeg',
      }).then(sentAudio => {
         // –û–±–Ω–æ–≤–ª—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø–æ—Å–ª–µ–¥–Ω–µ–º –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–Ω–æ–º –∞—É–¥–∏–æ
         sentAudios[selectedFile] = sentAudio.message_id;
      }).catch(error => {
         console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ –∞—É–¥–∏–æ:', error);
         bot.sendMessage(chatId, '–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ –º—É–∑—ã–∫–∏: ' + error.message);
      });
   });
}


// –õ–æ–≥–∏–∫–∞ —Ä–∞—Å—á–µ—Ç–∞ —á–∏—Å–ª–∞ –¥—É—à–∏
function calculateLifePathNumber(birthdate) {
   const nums = birthdate.split('.').join('');
   let sum = 0;
   for (let i = 0; i < nums.length; i++) {
      sum += parseInt(nums[i], 10);
   }
   while (sum > 9) {
      sum = sum.toString().split('').reduce((acc, curr) => acc + parseInt(curr, 10), 0);
   }
   return sum;
}


//–õ–æ–≥–∏–∫–∞ —Ä–∞—Å—á–µ—Ç–∞ —á–∏—Å–ª–∞ —Å—É–¥—å–±—ã
function calculateSoulNumber(birthDay) {
   let sum = birthDay.split('').reduce((acc, val) => acc + parseInt(val, 10), 0);
   while (sum > 9) {
      sum = sum.toString().split('').reduce((acc, val) => acc + parseInt(val, 10), 0);
   }
   return sum;
}



